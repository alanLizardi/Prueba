package cl.accenture.prueba.cl.accenture.prueba.modelo;

public enum Level {
	NEWASSOCIATE("NEW ASSOCIATE"), ASSOCIATE("ASSOCIATE"), ASSIST("ASSIST"), ANALIST("ANALISTE"), MANAGER("MANAGER"),
	SENIORMANAGER("SENIORMANAGER"), PARTNER("PARTNER"), PRESIDENT("PRESIDENT");

	private String nombre;

	private Level(String nombre) {
		this.nombre = nombre;
	}

	public String getNombre() {
		return nombre;
	}

}
